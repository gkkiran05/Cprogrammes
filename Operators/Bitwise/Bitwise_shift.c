//bit wise operator using >> <<
#include <stdio.h>

int main()
{
	//variable declaration
	int a, b;

	//Main Logic
	a = -208 >> 2;
	b = -25 << 3;

	//printing out put
	printf("value of -208 >> 2 is : %d\n", a);
	printf("value of -25 << 3 is : %d\n", b);

	return 0;
}






/*
 * value of -208 >> 2 is : -52
   value of -25 << 3 is : -200
   */


